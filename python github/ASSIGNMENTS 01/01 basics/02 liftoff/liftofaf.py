def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):
        print(i, end=" ")  # Print the countdown number followed by a space
    
    # After the countdown, print "Liftoff!"
    print("Liftoff!")

# Call the main function to run the program
if __name__ == "__main__":
    main()
